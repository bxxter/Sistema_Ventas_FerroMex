    private void cargarDatosUsuario() {
        Usuario usuario = Sesion.getUsuarioActual();

        if (usuario != null) {
            tarjetaPerfil1.setNombre(usuario.getNombre());
            tarjetaPerfil1.setCorreo(usuario.getEmail());
            tarjetaPerfil1.setRol(usuario.getRol());

            switch (usuario.getRol().toLowerCase()) {
                case "admin":
                    tarjetaPerfil1.setDescripcion("Administrador del sistema con todos los permisos.");
                    tarjetaPerfil1.setImagenPerfilDesdeRecurso("/img/clientes.png");
                    break;
                case "cajero":
                    tarjetaPerfil1.setDescripcion("Encargado de las operaciones de caja.");
                    tarjetaPerfil1.setImagenPerfilDesdeRecurso("/img/user.png");
                    break;
                case "supervisor":
                    tarjetaPerfil1.setDescripcion("Supervisa el funcionamiento y al personal.");
                    tarjetaPerfil1.setImagenPerfilDesdeRecurso("/img/user.png");
                    break;
                default:
                    tarjetaPerfil1.setDescripcion("Rol no reconocido");
                    tarjetaPerfil1.setImagenPerfilDesdeRecurso("/img/user.png");
            }
        }
    }

